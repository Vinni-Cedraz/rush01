int	lines_are_valid(char matr[6][6])
{
	int	x;

	x = 1;
	while (x <= 4)
	{
		if (matr[x][0] == '4' && matr[x][5] >= '2')
			return (0);
		if ((matr[x][0] == '3' || matr[x][0] == '2') && matr[x][5] == '4')
			return (0);
		if (matr[x][0] == '1' && matr[x][5] == '1')
			return (0);
		if (matr[x][0] == '3' && matr[x][5] == '3')
			return (0);
		x++;
	}
	return (1);
}
